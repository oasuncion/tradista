CREATE table TRANSFER(
ID BIGINT PRIMARY KEY GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1), 
TYPE VARCHAR(7) NOT NULL,
STATUS VARCHAR(8) NOT NULL,
DIRECTION VARCHAR(7),
QUANTITY  DECIMAL(30,15),
PURPOSE VARCHAR(50) NOT NULL,
TRADE_ID BIGINT,
BOOK_ID BIGINT NOT NULL,
CREATION_DATETIME TIMESTAMP NOT NULL,
FIXING_DATETIME TIMESTAMP,
SETTLEMENT_DATE DATE NOT NULL,
CURRENCY_ID BIGINT,
PRODUCT_ID BIGINT,
CONSTRAINT FK_TRANSFER_TRADE_ID FOREIGN KEY (TRADE_ID) REFERENCES TRADE(ID),
CONSTRAINT FK_TRANSFER_BOOK_ID FOREIGN KEY (BOOK_ID) REFERENCES BOOK(ID),
CONSTRAINT FK_TRANSFER_CURRENCY_ID FOREIGN KEY (CURRENCY_ID) REFERENCES CURRENCY(ID),
CONSTRAINT FK_TRANSFER_PRODUCT_ID FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCT(ID));